package quiz09;

public class SellFruit {
	private int allPay;
	private int allAmount;
	private int appleAmount;
	private int orangeAmount;
	private int bananaAmount;
	
	public int getAppleAmount() {
		return appleAmount;
	}
	public void setAppleAmount(int appleAmount) {
		this.appleAmount = appleAmount;
	}
	public int getOrangeAmount() {
		return orangeAmount;
	}
	public void setOrangeAmount(int orangeAmount) {
		this.orangeAmount = orangeAmount;
	}
	public int getBananaAmount() {
		return bananaAmount;
	}
	public void setBananaAmount(int bananaAmount) {
		this.bananaAmount = bananaAmount;
	}
	public int getAllPay() {
		return allPay;
	}
	public void setAllPay(int allPay) {
		this.allPay = allPay;
	}
	public int getAllAmount() {
		return allAmount;
	}
	public void setAllAmount(int allAmount) {
		this.allAmount = allAmount;
	}
}