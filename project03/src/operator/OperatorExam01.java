package operator;

public class OperatorExam01 {
	public static void main(String[] args) {
		// 산술 연산자
		// 더하기 : +
		// 빼기 : -
		// 곱하기 : *
		// 나누기 : / (나눈 몫에 해당하는 값)
		// 나머지 : % (많이 사용하는 연산자 중 하나)
		
		int num1 = 10;
		int num2 = 3;
		
		System.out.println(num1 + num2);
		System.out.println(num1 - num2);
		System.out.println(num1 * num2);
		System.out.println(num1 / num2);
		System.out.println(num1 % num2);
	}

}
