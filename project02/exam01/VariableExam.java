package exam01;

public class VariableExam {
	public static void main(String[] args) {
		// 변수 선언 : 데이터 타입 + 변수명
		// 데이터 타입 : 기본형 int, double, char, boolean
		// 변수명 : 숫자로 시작할수 없고 자바 예약어는 사용 불가
		
		// 정수형 변수 선언
		int number;
		// 초기화는 변수에 값을 저장하는 것을 의미한다.
		// 선언 후 초기화
		number = 30;
		
		System.out.println(number);
		
		// 선언과 동시에 초기화
		int number2 = 100;
		System.out.println(number2);
	}
}
